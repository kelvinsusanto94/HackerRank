def countApplesAndOranges(s, t, a, b, apples, oranges):
    countApple = 0;
    countOrange = 0;
    for i in apples:
        temp = a + i
        if (s<= temp <= t):
            countApple+=1

    for j in oranges:
        temp = b + j
        if (s<= temp <= t):
            countOrange+=1

    print(countApple)
    print(countOrange)
    return


